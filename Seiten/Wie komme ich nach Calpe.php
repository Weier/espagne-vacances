	<h3>Mit dem Auto</h3> 
	<p>
	Sie brauchen von Lausanne aus zwischen 13 und 15 Stunden um nach Calpe zu kommen. Es sind rund 1'400km. Nebst den Benzinkosten d&uuml;rfen die Autobahngeb&uuml;hren in Frankreich und Spanien nicht vergessen werden. 
	</p> 
