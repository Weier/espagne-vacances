		 <li><a href="Photogalerie/Ausfluege/Ifach/index_de.html" target="_blank">Ifach</a><p>
		 Der Felsen von Ifach ist das unverkennbaren Wahrzeichen der Costa Blanca. Er ist in ein Naturpark, ragt 332 Meter aus dem Meer heraus und schf&uuml;tzt eine Vielzahl von seltenen Gew&auml;chsen und V&ouml;geln. Mit guten Schuhen ist der Gipfel auf einem Fussweg durch einen, in den Felsen geschlagenen, Tunnel erreichbar. An klaren Tagen wird man mit einem wundervollen Panorama entlang der Kf&uuml;ste bis nach Ibiza belohnt.
		 </p>
		 </li>
		 <li><a onclick="window.open(this.href,'_blank','width=1536,height=1024,scrollbars=no,resizable=no,toolbar=no,directories=no,location=no,menubar=no,status=no,left=0,top=0');return false"  href="Photogalerie/Ausfluege/Aqualandia/index_de.html"> Aqualandia</a> <p>
