		 <li>Palmenwald<p>
		 Der Palmengarten Palmeral de Elx von Elche ist, wie der Name schon sagt, ein Palmengarten in dem Sie viele Arten von Palmen sehen kf&uuml;nnen.
		 </p>
		 </li>
		 <li>Fuentes del Algar<p>
         El Algar ist ein Fluss, der mitten im Aitana Gebirge entspringt. Noch vor Jahren war dieses malerische Wildbachtal mit dem klaren Quellwasser ein Geheimtip. Mittlerweile ist auch dieses Gebiet erschlossen. Es wurden Wege angelegt, Lichter installiert und natf&uuml;rlich Eintritt erhoben. Letztendlich bleibt aber doch ein riesiger Badespass bei 10 Grad Wassertemperatur.
		 </p><p>
         Zu erreichen ist das Tal von Calpe aus Richtung Alicante. Folgen Sie der Autobahnauffahrt vor Altea (Richtung Alicante Valencia). Nicht auf die Autobahn fahren sondern nach Callosa d'en Sarria. In Callosa, der Honig-(unbedingt welchen mitnehmen)und Nisperostadt folgen sie dem Wegweiser El Algar. Wegstrecke von Calpe ca 30. Minuten.
		 </p></li>
